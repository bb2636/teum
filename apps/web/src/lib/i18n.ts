const translations: Record<Language, Record<string, string>> = { ko, en };

export function getCurrentLanguage(): Language {
  if (typeof window === "undefined") return DEFAULT_LANGUAGE;
  const saved = localStorage.getItem("teum_language") as Language | null;
  if (saved && translations[saved]) return saved;
  const browserLang = navigator.language?.split("-")[0];
  if (browserLang === "en") {
    localStorage.setItem("teum_language", "en");
    return "en";
  }
  localStorage.setItem("teum_language", DEFAULT_LANGUAGE);
  return DEFAULT_LANGUAGE;
}

export function setLanguage(language: Language): void {
  if (typeof window === "undefined") return;
  localStorage.setItem("teum_language", language);
}

export function t(
  key: string,
  params?: Record<string, string | number>,
): string {
  const lang = getCurrentLanguage();
  let text =
    translations[lang]?.[key] || translations[DEFAULT_LANGUAGE]?.[key] || key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
}
